package main

import "fmt"

func main() {

	fmt.Println("Year Born       Year Graduated            Result")

	// addition
	fmt.Println("2009         +       2027                 = " + fmt.Sprint(2009+2027))

	// subtraction
	fmt.Println("2009         -       2027                 = " + fmt.Sprint(2009-2027))

	// multiplication
	fmt.Println("2009         *       2027                 = " + fmt.Sprint(2009*2027))

	// division
	fmt.Println("2009         /       2027                 = " + fmt.Sprint(2009/2027))

	// modulo
	fmt.Println("2009         %       2027                 = " + fmt.Sprint(2009%2027))

	fmt.Println("\nDone.")

}
